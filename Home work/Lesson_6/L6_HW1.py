#Програма, що зчитує числа від користувача поки не буде введено ключове слово sum.

print('''
              Вкажіть ваше число, 
або якщо хочете закінчити і отримати результат введіть "sum"
''')

y = 0
while True:
    x = input("Введіть число >> ")
    if x == "sum":
        print(y)
        break
    else:
        try:
            y += float(x)
        except:
            print("Введіть число або sum будь-ласка!")